""" We can use +, -, *, /, **, //, % operators to perform mathematical operations in Python. """

# Addition

num1 = 10
num2 = 5
num3 = 2.5

print(num1 + num2 + num3) # 17.5

# Subtraction   

num1 = 10
num2 = 5
num3 = 2.5

print(num1 - num2 - num3) # 2.5

# Multiplication 

num1 = 10
num2 = 5
num3 = 2.5

print(num1 * num2 * num3) # 125.0

# Division

num1 = 10
num2 = 5
num3 = 2.5

print(num1 / num2 / num3) # 0.8

# Exponentiation

num1 = 10
num2 = 5
num3 = 2.5

print(num1 ** num2 ** num3) # 100000.0

# Floor Division

num1 = 10
num2 = 6

print(num1 // num2) # 1

# Modulus

num1 = 10
num2 = 5

print(num1 % num2) # 0 