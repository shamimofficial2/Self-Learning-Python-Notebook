""" In Python we can use if, elif, and else statements to control the flow of our program based on certain conditions."""

# Example of if, elif, and else statements

name = "Shamim Ahmed Nahid"
programming_language = "Python"

if name == "Shamim Ahmed Nahid": # if is a statement that checks a condition 
    print("Hello, Shamim Ahmed Nahid! you will be a great programmer.") # if block executes if the condition is True

elif programming_language == "Python": # elif is a statement that checks another condition if the previous one was False
    print("Hello, you are learning Python!") # elif block executes if its condition is True

else: # else is a statement that executes if all previous conditions were False
    print("Hello, you are not Shamim Ahmed Nahid let's learn Python together!") # else block executes if all previous conditions were False